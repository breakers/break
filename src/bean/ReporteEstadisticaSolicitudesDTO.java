package bean;

public class ReporteEstadisticaSolicitudesDTO {
	private int cantidadPendientes;
	private int cantidadAprobadas;
	private int cantidadRechazadas;
	private int cantidadContratadas;
	
	
	public int getCantidadPendientes() {
		return cantidadPendientes;
	}
	public void setCantidadPendientes(int cantidadPendientes) {
		this.cantidadPendientes = cantidadPendientes;
	}
	public int getCantidadAprobadas() {
		return cantidadAprobadas;
	}
	public void setCantidadAprobadas(int cantidadAprobadas) {
		this.cantidadAprobadas = cantidadAprobadas;
	}
	public int getCantidadRechazadas() {
		return cantidadRechazadas;
	}
	public void setCantidadRechazadas(int cantidadRechazadas) {
		this.cantidadRechazadas = cantidadRechazadas;
	}
	public int getCantidadContratadas() {
		return cantidadContratadas;
	}
	public void setCantidadContratadas(int cantidadContratadas) {
		this.cantidadContratadas = cantidadContratadas;
	}
	
	
}
