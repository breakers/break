package bean;

public class FiltroClienteDTO {
	
	private int tipoPersona;
	private String nomcli;
	private String numdoc;
	private String ruc;
	private String razsocial;
	private String nombreRepre;
	private String apeRepre;
	
	
	
	public int getTipoPersona() {
		return tipoPersona;
	}
	public void setTipoPersona(int tipoPersona) {
		this.tipoPersona = tipoPersona;
	}
	public String getNomcli() {
		return nomcli;
	}
	public void setNomcli(String nomcli) {
		this.nomcli = nomcli;
	}
	public String getNumdoc() {
		return numdoc;
	}
	public void setNumdoc(String numdoc) {
		this.numdoc = numdoc;
	}
	public String getRuc() {
		return ruc;
	}
	public void setRuc(String ruc) {
		this.ruc = ruc;
	}
	public String getRazsocial() {
		return razsocial;
	}
	public void setRazsocial(String razsocial) {
		this.razsocial = razsocial;
	}
	public String getNombreRepre() {
		return nombreRepre;
	}
	public void setNombreRepre(String nombreRepre) {
		this.nombreRepre = nombreRepre;
	}
	public String getApeRepre() {
		return apeRepre;
	}
	public void setApeRepre(String apeRepre) {
		this.apeRepre = apeRepre;
	}
	
	
	

}
